    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (tfUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong");
        } else if (tfTelepon.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nomor Telepon tidak boleh kosong");
        } else if (tfEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Email tidak boleh kosong");
        } else if (tfPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
        } else {
            String sql = "insert into tb_admin (username, telepon, email, password, level) values (?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, tfUsername.getText());
                stat.setString(2, tfTelepon.getText());
                stat.setString(3, tfEmail.getText());
                stat.setString(4, tfPassword.getText());
                stat.setString(5, (String) cmbLevel.getSelectedItem());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
                dataTable();
                resett();

                tfUsername.requestFocus();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal Disimpan : " + e);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

