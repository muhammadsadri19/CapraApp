    public void dataTable() {
        Object[] Baris = {"No", "ID", "Username", "Telepon", "Email", "Password", "Level"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelUser.setModel(tabmode);
        String sql = "select * from tb_admin order by id_admin asc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id_admin = hasil.getString("id_admin");
                String username = hasil.getString("username");
                String telepon = hasil.getString("telepon");
                String email = hasil.getString("email");
                String password = hasil.getString("password");
                String level = hasil.getString("level");
                String[] data = {"", id_admin, username, telepon, email, password, level};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data tidak bisa ditampilkan");
        }
    }

    public void pencarian(String sql) {
        Object[] Baris = {"No", "ID", "Username", "Telepon", "Email", "Password", "Level"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelUser.setModel(tabmode);

        int baris = tabelUser.getRowCount();
        for (int i = 0; 1 < baris; i++) {
            tabmode.removeRow(i);
        }

        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id_admin = hasil.getString("id_admin");
                String username = hasil.getString("username");
                String telepon = hasil.getString("telepon");
                String email = hasil.getString("email");
                String password = hasil.getString("password");
                String level = hasil.getString("level");
                String[] data = {"", id_admin, username, telepon, email, password, level};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data tidak bisa ditampilkan");
        }
    }

