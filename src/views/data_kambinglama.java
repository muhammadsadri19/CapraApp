    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        String sql = "update tb_kambinglama set kode_kambing=?, umur=?, kesehatan=?, nama_kategori=?, nama_investor=? where kode_kambing='" + tfKodeKambing.getText()+ "'";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tfKodeKambing.getText());
            stat.setString(2, tfUmur.getText());
            stat.setString(3, tfKesehatan.getText());
            stat.setString(4, (String) cmbKategori.getSelectedItem());
            stat.setString(5, tfPemilik.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah! ");
            dataTable();
            resett();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal diubah : " + e);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from tb_kambinglama where kode_kambing ='" + tfKodeKambing.getText() + "'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                dataTable();
                resett();
                tfKodeKambing.requestFocus();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + e);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

