    public void dataTable() {
        Object[] Baris = {"No","Kode Kambing", "Umur", "Kesehatan", "Kategori", "Pemilik"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelKambing.setModel(tabmode);
        String sql = "select * from tb_kambinglama order by kode_kambing asc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String kode_kambing = hasil.getString("kode_kambing");
                String umur = hasil.getString("umur");
                String kesehatan = hasil.getString("kesehatan");
                String nama_kategori = hasil.getString("nama_kategori");
                String nama_investor = hasil.getString("nama_investor");
                String[] data = {"",kode_kambing, umur, kesehatan,  nama_kategori, nama_investor};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data tidak bisa ditampilkan");
        }
    }
    
    
    
    
    public void pencarian(String sql) {
        Object[] Baris = {"No","Kode Kambing", "Umur", "Kesehatan", "Kategori", "Pemilik"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelKambing.setModel(tabmode);

        int baris = tabelKambing.getRowCount();
        for (int i = 0; 1 < baris; i++) {
            tabmode.removeRow(i);
        }

        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String kode_kambing = hasil.getString("kode_kambing");
                String umur = hasil.getString("umur");
                String kesehatan = hasil.getString("kesehatan");
                String nama_kategori = hasil.getString("nama_kategori");
                String nama_investor = hasil.getString("pemilik");
                String[] data = {"",kode_kambing, umur, kesehatan,  nama_kategori, nama_investor};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data tidak bisa ditampilkan");
        }
    }
    

