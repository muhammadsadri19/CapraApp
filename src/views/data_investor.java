    public void dataTable() {
        Object[] Baris = {"No", "ID", "Nama Investor", "Telepon Investor", "Alamat Investor", "Kode Kambing"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelUser.setModel(tabmode);
        String sql = "select * from tb_investor order by id_investor asc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id_investor = hasil.getString("id_investor");
                String nama_investor = hasil.getString("nama_investor");
                String telepon_investor = hasil.getString("telepon_investor");
                String alamat_investor = hasil.getString("alamat_investor");
                String kode_kambing = hasil.getString("kode_kambing");
                String[] data = {"", id_investor, nama_investor, telepon_investor, alamat_investor, kode_kambing};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data tidak bisa ditampilkan");
        }
    }

    public void pencarian(String sql) {
        Object[] Baris = {"No", "ID", "Nama Investor", "Telepon Investor", "Alamat Investor", "Kode Kambing"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelUser.setModel(tabmode);

        int baris = tabelUser.getRowCount();
        for (int i = 0; 1 < baris; i++) {
            tabmode.removeRow(i);
        }

        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id_investor = hasil.getString("id_investor");
                String nama_investor = hasil.getString("nama_investor");
                String telepon_investor = hasil.getString("telepon_investor");
                String alamat_investor = hasil.getString("alamat_investor");
                String kode_kambing = hasil.getString("kode_kambing");
                String[] data = {"", id_investor, nama_investor, telepon_investor, alamat_investor, kode_kambing};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data tidak bisa ditampilkan");
        }
    }

