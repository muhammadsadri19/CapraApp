    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (tfNamaInvestor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong");
        } else if (tfTeleponInvestor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nomor Telepon Investor tidak boleh kosong");
        } else if (tfAlamatInvestor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Alamat Investor tidak boleh kosong");
        } else if (tfKodeKambing.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
        } else {
            String sql = "insert into tb_investor (nama_investor, telepon_investor, alamat_investor, kode_kambing, level) values (?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, tfNamaInvestor.getText());
                stat.setString(2, tfTeleponInvestor.getText());
                stat.setString(3, tfAlamatInvestor.getText());
                stat.setString(4, tfKodeKambing.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
                dataTable();
                resett();

                tfNamaInvestor.requestFocus();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal Disimpan : " + e);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

